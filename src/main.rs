fn main() {
    let n = 35;

    if n < 30 {
        println!("O número é menor que 30!");
    } else if n > 39 {
        println!("O número é maior que 39!");
    } else {
        println!("O número não é menor que 30 ou maior que 39!");
    }

    if n == 20 {
        println!("O número é igual a 20!");
    }
}
